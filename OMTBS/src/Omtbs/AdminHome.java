    private boolean PasswordCurrentCheck(){
        String currentpass = String.valueOf(CurrentPasswordField.getPassword());
        String currentconfirmpass = String.valueOf(ConfirmCurrentPasswordField.getPassword());
        if(currentpass.equals(currentconfirmpass)){
            ConfirmCurrentPasswordField.setBackground(Color.GREEN);
            return true;
        }
        else if("".equals(currentconfirmpass)){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null,"Please Re-Enter The Password!", "Current Password...", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        else {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null,"Please Enter The Same Password!", "Same Current Password...", JOptionPane.INFORMATION_MESSAGE);
            ConfirmCurrentPasswordField.setBackground(new Color(246, 53, 40));
            return false;
        }
    }
    
