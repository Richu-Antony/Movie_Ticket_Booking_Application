    private void PasswordUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PasswordUpdateButtonActionPerformed
        boolean APCL = PasswordNewLength();
        boolean APCC = PasswordCurrentCheck();
        boolean APNC = PasswordNewCheck();
        String ahaname = AdminHomeUserLabel.getText();
        String currentpass = String.valueOf(CurrentPasswordField.getPassword());
        String currentconfirmpass = String.valueOf(ConfirmCurrentPasswordField.getPassword());
        String newpass = String.valueOf(NewPasswordField.getPassword());
        String confirmnewpass = String.valueOf(ConfirmNewPasswordField.getPassword());
        if(currentpass.equals("")){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Please Enter The Current Password", "Current Password", JOptionPane.WARNING_MESSAGE);
        }
        else if(currentconfirmpass.equals("")){
             Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Please Confirm The Current Password", "Confirm Current Password", JOptionPane.WARNING_MESSAGE);
        }
        else if(newpass.equals("")){
             Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Please Enter The New Password", "New Password", JOptionPane.WARNING_MESSAGE);
        }
        else if(confirmnewpass.equals("")){
             Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Please Confirm The New Password", "Confirm New Password", JOptionPane.WARNING_MESSAGE);
        }
        else if(APCL==true && APCC==true && APNC==true){
            try {
                String adminpassupquery ="UPDATE `admin` SET ADMIN_PASSWORD WHERE `ADMIN_NAME` = ? AND `ADMIN_PASSWORD` = ?";
                PreparedStatement ps = MyConnection.getConnection().prepareStatement(adminpassupquery);
                ps.setString(1, ahaname);
                ps.setString(2, currentpass);
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null,"Admin Password Has Been Successfully Updated" , "Updating Admin Password", JOptionPane.INFORMATION_MESSAGE);
                    ConfirmCurrentPasswordField.setText("");
                    NewPasswordField.setText("");
                    ConfirmNewPasswordField.setText("");
                }
            } 
            catch (SQLException ex) {
                Logger.getLogger(AdminHome.class.getName()).log(Level.SEVERE, null, ex);
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(null, "Reset Password Error Occuring!", "Password Reset Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Incorrect Username Or Password", "Username Or Password", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_PasswordUpdateButtonActionPerformed

