                    vr2.add(rs.getString("MOVIE_CREATED_ADMIN"));
                    vr2.add(rs.getString("MOVIE_THEATRE"));
                    vr2.add(rs.getString("MOVIE_NAME"));
                    vr2.add(rs.getString("MOVIE_DESC"));
                    vr2.add(rs.getString("MOVIE_PRICE"));
                    vr2.add(rs.getString("MOVIE_NO_OF_SEATS"));
                    vr2.add(rs.getString("MOVIE_TIME"));
                    vr2.add(rs.getString("MOVIE_DATE"));
                    vr2.add(rs.getString("MOVIE_LOCATION"));
                    vr2.add(rs.getString("MOVIE_LANGUAGE"));
