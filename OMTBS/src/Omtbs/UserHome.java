    private void LoginUserFieldFetch(){
        try {
            String loginusername =  UserHomeUserLabel.getText();
            // String loginusername = "Richu Antony";
            String useradminfieldfetchquery ="SELECT `USER_NAME`, `USER_EMAILID`, `USER_MOBILE_NO`, `USER_DOB`, `USER_GENDER` , `USER_ADDRESS` , `USER_ACCOUNT_TIMESTAMP` FROM `user` WHERE `USER_NAME` = ? ";
            PreparedStatement ps = MyConnection.getConnection().prepareStatement(useradminfieldfetchquery);
            ps.setString(1, loginusername);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                UHUsernameTextField.setText(rs.getString(1));
                UHEmailidTextField.setText(rs.getString(2));
                UHMobileNoTextField.setText(rs.getString(3));
                UHDobTextField.setText(rs.getString(4));
                UHGenderTextField.setText(rs.getString(5));
                UHAddressTextArea.setText(rs.getString(6)); 
                UHTimestampTextField.setText(rs.getString(7));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UserHome.class.getName()).log(Level.SEVERE, null, ex);
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null,"Login User Details  Fetching Error Occuring!" , "User Details Fetching", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void MoviesFieldFetch(){
        int r;
        try {
            
            String moviefieldfetchquery ="SELECT *FROM `moviedetails`";
            PreparedStatement ps = MyConnection.getConnection().prepareStatement(moviefieldfetchquery);
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            r = rsmd.getColumnCount();
            DefaultTableModel Dft = (DefaultTableModel)UHMovieDetailsTable.getModel();
            Dft.setRowCount(0);
            while(rs.next()){
                Vector vr2 = new Vector();
                for(int a=1; a<=r; a++){
                    vr2.add(rs.getString("MOVIE_ID"));
                    vr2.add(rs.getString("MOVIE_NAME"));
                    vr2.add(rs.getString("MOVIE_DESC"));
                }
                Dft.addRow(vr2);
            }   
        } catch (SQLException ex) {
            Logger.getLogger(UserHome.class.getName()).log(Level.SEVERE, null, ex);
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null,"Movies Filed Fetching Error Occuring!" , "Movies Fetching", JOptionPane.ERROR_MESSAGE);

        }
    }
    
