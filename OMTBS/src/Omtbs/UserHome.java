    private void UserFeedbackSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UserFeedbackSubmitButtonActionPerformed
        String feedbacksubmit = UserFeedbackTextArea.getText();
        String feedbackadmin = UserHomeUserLabel.getText();
        if(feedbacksubmit.equals("")){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Please Enter The Feedback Before Submiting", "Feedback Submition", JOptionPane.WARNING_MESSAGE);
        }
        else{
            try {
                String FeedbacksubmitQuery = "INSERT INTO `feedback`(`FEEDBACK_USER`, `FEEDBACK`) VALUES (?,?)";
                PreparedStatement ps = MyConnection.getConnection().prepareStatement(FeedbacksubmitQuery);
                ps.setString(1, feedbackadmin);
                ps.setString(2, feedbacksubmit);
                if(ps.executeUpdate() > 0){
                    JOptionPane.showMessageDialog(null, "Feedback Submitted Succesfully", "Feedback Submited", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Feedback Submition Failed!", "Feedback Failed", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            catch (SQLException ex) {
                Logger.getLogger(UserHome.class.getName()).log(Level.SEVERE, null, ex);
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(null, "Feedback Submition Error Occuring!", "Feedback Error", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_UserFeedbackSubmitButtonActionPerformed

